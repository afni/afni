int run_stcorr2( int comline, PARAMS_stcorr2 opts,
                 int argc, char *argv[] )
{
   int i, j, k, idx;
   int nn;
   int tmp;

   int *Dim=NULL;
   int nvox, nvalsA, nvalsB;

	THD_3dim_dataset *dset_insetA = NULL;     // insetA
	THD_3dim_dataset *dset_insetB = NULL;     // insetB
   THD_3dim_dataset *dset_mask   = NULL;     // mask
   THD_3dim_dataset *dset_out    = NULL;     // output dset (to be created)

   char prefix[THD_MAX_PREFIX];

   byte *mask_arr = NULL;                    // what mask becomes
   int nmask = -1;

   float *tsA = NULL;                        // time series, from insetA
   float *tsB = NULL;                        // time series, from insetB
   float **AZ = NULL;                        // insetA -> masked and Zscored
   float **BZ = NULL;                        // insetB -> masked and Zscored

   float *DOT = NULL;                        // dot product array
   float *DOTOUT = NULL;                     // dot product array, to output

   /* ---------------------------------------------------------------------- */

   ENTRY("run_SpaceTimeCorr2");

   /* -------------------------- verify inputs ----------------------------- */

   // insetA
   dset_insetA = THD_open_dataset(opts.insetA);
   if( dset_insetA == NULL )
      ERROR_exit("Can't open dataset '%s'", opts.insetA);
   DSET_load(dset_insetA); CHECK_LOAD_ERROR(dset_insetA);

   // insetB
   dset_insetB = THD_open_dataset(opts.insetB);
   if( dset_insetB == NULL )
      ERROR_exit("Can't open dataset '%s'", opts.insetB);
   DSET_load(dset_insetB); CHECK_LOAD_ERROR(dset_insetB);

   // mask
   dset_mask = THD_open_dataset(opts.mask_name);
   if( dset_mask == NULL )
      ERROR_exit("Can't open dataset '%s'", opts.mask_name);
   DSET_load(dset_mask); CHECK_LOAD_ERROR(dset_mask);

   // check for grid mismatches
   if( THD_dataset_mismatch( dset_insetA, dset_insetB ) )
      ERROR_exit("Mismatch between insetA and insetB dsets!\n");
   if( THD_dataset_mismatch( dset_insetA, dset_mask ) )
      ERROR_exit("Mismatch between insetA and mask dsets!\n");

   /* -------------------------- basic setup ----------------------------- */

   // spatial dim info
   Dim = (int *)calloc(3, sizeof(int));
   Dim[0] = DSET_NX(dset_insetA);
   Dim[1] = DSET_NY(dset_insetA); 
   Dim[2] = DSET_NZ(dset_insetA);
   nvox   = DSET_NVOX(dset_insetA);

   // time dim: need not be equal
   nvalsA = DSET_NVALS(dset_insetA);
   nvalsB = DSET_NVALS(dset_insetB);

   // mask comes from just [0]th vol (NB: mask_arr has len = nvox)
   mask_arr = THD_makemask( dset_mask, 0, 0, -1 );
   // number of nonzero voxels in a mask
   nmask = THD_countmask( nvox, mask_arr );
   if( opts.verb )
      INFO_message("Relative number of voxels in mask = %d / %d", nmask, nvox);
   if( nmask < 1 )
      ERROR_exit("Automask is too small (i.e., empty) to process");

   // allocate 2D arrays to hold each inset, masked and transformed to Z
   AZ = calloc( nmask, sizeof(AZ));
   for( i=0 ; i<nmask ; i++) 
      AZ[i] = calloc( nvalsA, sizeof(float)); 
   BZ = calloc( nmask, sizeof(BZ));
   for( i=0 ; i<nmask ; i++) 
      BZ[i] = calloc( nvalsA, sizeof(float)); 

   if( (AZ == NULL) || (BZ == NULL) ) { 
      ERROR_message("MemAlloc failure for AZ and/or BZ.");
      exit(4);
   } 
   
   // allocate 1D arrays to hold time series from respective insets
   tsA = (float *)calloc(nvalsA, sizeof(float));
   tsB = (float *)calloc(nvalsB, sizeof(float));

   if( (tsA == NULL) || (tsB == NULL) ) { 
      ERROR_message("MemAlloc failure for tsA and/or tsB.");
      exit(4);
   } 

   /* -------------------------- Z-score inputs ---------------------------- */

   /*
     Populate AZ and BZ (Z-scored time series within mask).
     
     Go through all voxels in each inset within mask, make Z-scores
     and save in arrays.
   */
   INFO_message("Z-score-ize the time series");

   idx = 0;
   for( i=0 ; i<nvox ; i++) {
      if( mask_arr[i] ) {

         // extract [i]th insetA time series (from within mask)
         tmp = THD_extract_float_array(i, dset_insetA, tsA); 
         // ... and convert it to Z-scores, and store it in [idx]th location
         tmp = zscore_ts_welford(tsA, AZ[idx], nvalsA, 1);

         // extract [i]th insetB time series (from within mask)
         tmp = THD_extract_float_array(i, dset_insetB, tsB); 
         // ... and convert it to Z-scores, and store it in [idx]th location
         tmp = zscore_ts_welford(tsB, BZ[idx], nvalsB, 1);

         idx++;
      }
   }

   // free some memory space
   if( dset_insetA ){
      DSET_delete(dset_insetA); 
      free(dset_insetA); 
   }

   if( dset_insetB ){
      DSET_delete(dset_insetB); 
      free(dset_insetB); 
   }

   /* -------------------------- SpaceTimeCorr ---------------------------- */

   // allocate 1D array to hold output dot product (the Pearson corr);
   // this has len = nmask
   DOT = (float *)calloc(nmask, sizeof(float));
   // ... and this is the one that gets filled in, to write out
   DOTOUT = (float *)calloc(nvox, sizeof(float));

   if( (DOT == NULL) || (DOTOUT == NULL) ) { 
      ERROR_message("MemAlloc failure for DOT and/or DOTOUT.");
      exit(4);
   }
   
   // do spacetime calc
   tmp = calc_spacetimecorr(DOT, nmask, AZ, nvalsA, BZ, nvalsB);

   // ... and fill those values into an array for output size
   idx = 0;
   for( i=0 ; i<nvox ; i++) {
      if( mask_arr[i] ) {
         // can do Fisher Z-transform of correlation values
         if( opts.out_Zcorr ) 
            DOTOUT[i] = BOBatanhf(DOT[idx]);
         else
            DOTOUT[i] = DOT[idx];
         idx++;
      }
   }
   
   /* ------------------------ write output dset --------------------------- */

   dset_out = EDIT_empty_copy( dset_mask ); 
   EDIT_dset_items(dset_out,
                   ADN_nvals, 1,
                   ADN_datum_all, MRI_float,    
                   ADN_prefix, opts.prefix,
                   ADN_none );

   if( !THD_ok_overwrite() && THD_is_ondisk(DSET_HEADNAME(dset_out)) )
      ERROR_exit("Can't overwrite existing dataset '%s'",
                 DSET_HEADNAME(dset_out));

   EDIT_substitute_brick(dset_out, 0, MRI_float, DOTOUT); 
   DOTOUT=NULL;

   // write out dset
   THD_load_statistics( dset_out );
   tross_Make_History("3dSpaceTimeCorr2", argc, argv, dset_out);
   THD_write_3dim_dataset(NULL, NULL, dset_out, True);

   /* --------------------------- free stuff ------------------------------ */

   if( dset_mask ){
      DSET_delete(dset_mask); 
      free(dset_mask); 
   }

   if( dset_out ){
      DSET_delete(dset_out); 
      free(dset_out); 
   }

   if( Dim )
      free(Dim);

   if( mask_arr )
      free(mask_arr);

   if( AZ ) {
      for( i=0 ; i<nmask ; i++) 
         free(AZ[i]);
      free(AZ);
   }

   if( BZ ) {
      for( i=0 ; i<nmask ; i++) 
         free(BZ[i]);
      free(BZ);
   }

   if( tsA )
      free(tsA);
   
   if (tsB )
      free(tsB);

   if( DOT )
      free(DOT);

   return 0;
}


/*
  SpaceTimeCalc, with OpenMP

  Parameters:
  DOT    : 1D arr (float, len: nmask), output to store dot product values
  nmask  : int, number of spatial locations; [0]th dim of AZ and BZ
  AZ     : 2D arr (float, nmask x nvalsA), stack of time series from insetA
  nvalsA : int, number of time pts in AZ; [1]th dim of AZ
  BZ     : 2D arr (float, nmask x nvalsB), stack of time series from insetB
  nvalsB : int, number of time pts in BZ; [1]th dim of BZ

  DOT will be output.
*/
int calc_spacetimecorr(float *DOT, int nmask, float **AZ, int nvalsA, 
                       float **BZ, int nvalsB)
{
   int Nthreads=1;
   int j;

   int ApproxTenPerc=0;

#ifdef USE_OMP
   INFO_message("Start OMPing");
   AFNI_SETUP_OMP(0) ;
   Nthreads = omp_get_max_threads();
   INFO_message("OMP thread count = %d", Nthreads);
#else
   INFO_message("Not using OMP-- will be slow.");
#endif

   ApproxTenPerc = (int) (0.1*nmask)/Nthreads;
   INFO_message("ApproxTenPerc = %d", ApproxTenPerc);

   if( ApproxTenPerc == 0 ) {
      if( nmask < 100 )
         WARNING_message("Very small number of voxels to calculate! "
                         "Only %d??", nmask);
      else
         WARNING_message("Really?  %d voxels to analyze and %d threads??",
                         nmask, Nthreads);
      ApproxTenPerc = 1;
   }
   else if(ApproxTenPerc < 0 ) 
      ERROR_exit("ERROR! %d voxels to analyze and %d threads?? "
                    "This leads to ~10 percent value of %d?!??",
                    nmask, Nthreads, ApproxTenPerc);

   AFNI_OMP_START;
#pragma omp parallel //if(1)
   {
      int pp;
      int i, n;
      int ithr;
      int nprog=0;
      time_t t_start=time(NULL);  /* init to quiet whiny compiler */

      int tmp;
      float sval=0.0;
      float vecA[nmask-1], vecB[nmask-1];
      float vecAZ[nmask-1], vecBZ[nmask-1];

#ifdef USE_OMP
      ithr = omp_get_thread_num();
      if( !ithr ) {
         fprintf(stderr,"++ Nvox progress proxy count on thread[%d]: "
                 "start ...\n", ithr);
         t_start = time(NULL);
      }
#else
      fprintf(stderr,"++ Nvox progress proxy count on (only) thread: "
              "start ...\n", ithr);
      t_start = time(NULL);
#endif

#pragma omp for
      for( pp=0 ; pp<nmask ; pp++ ) {

         /* ----------------------- calc 1: time ----------------------- */
         
         /* vecA and vecB has len=nmask-1, because we don't include
            the i=pp case of the dot product; we therefore loop over
            nmask-1 values to store them easily in the [i]th spot, and
            put the case where i==nmask-1 where i=pp.  It's
            funny-looking, but just reduces need to add another index
         */
         for( i=0 ; i<nmask-1 ; i++ ) {
            vecA[i] = 0.0;
            vecB[i] = 0.0;
            if( i != pp ) {
               // dot product across time for A and B datasets
               for ( n=0 ; n<nvalsA ; n++ )
                  vecA[i]+= AZ[pp][n]*AZ[i][n];
               vecA[i]/= nvalsA;
               for ( n=0 ; n<nvalsB ; n++ ) 
                  vecB[i]+= BZ[pp][n]*BZ[i][n];
               vecB[i]/= nvalsB;
            }
         }
         // final one to put in: the last [i]th value gets stored in
         // the i=pp spot (which was already initialized to zero above)
         i = nmask-1;
         for ( n=0 ; n<nvalsA ; n++ )
            vecA[pp]+= AZ[pp][n]*AZ[i][n];
         vecA[pp]/= nvalsA;
         for ( n=0 ; n<nvalsB ; n++ ) 
            vecB[pp]+= BZ[pp][n]*BZ[i][n];
         vecB[pp]/= nvalsB;

         /* ----------------------- calc 2: space ----------------------- */

         // convert 1D arrays to Zscores, to prep for dot product
         tmp = zscore_ts_welford(vecA, vecAZ, nmask-1, 1);
         tmp = zscore_ts_welford(vecB, vecBZ, nmask-1, 1);

         // do the dot product
         sval = 0.0;
         for ( n=0 ; n<nmask ; n++ )
            sval+= vecAZ[n]*vecBZ[n];
         sval/= nmask-1;

         DOT[pp] = sval;

         /* -------------------- finalize/progress ---------------------- */

#ifdef USE_OMP
         ithr = omp_get_thread_num();
         // counter for user
         if( ithr == 0 ) {
            nprog++;
            if( (nprog % ApproxTenPerc) == 0 ) {
               fprintf(stderr,"\t%s %3.0f%% %s -> %.2f min\n",
                       "[", nprog *10./ApproxTenPerc,"]", 
                       (float) difftime( time(NULL), t_start)/60. );
            }
         }
#else
         nprog++;
         if( (nprog % ApproxTenPerc) == 0 ) {
            fprintf(stderr,"\t%s %3.0f%% %s -> %.2f min\n",
                    "[", nprog *10./ApproxTenPerc,"]", 
                    (float) difftime( time(NULL), t_start)/60. );
         }
#endif

      }

   } // end OMP
   AFNI_OMP_END ;
   
   
   INFO_message("Finished OMPing.  Finalizing...");


   return 0;

}

int main(int argc, char *argv[]) {

   int ii = 0;
   int iarg;
   PARAMS_stcorr2 InOpts;
   float tmp;
   int itmp;
   char *auto_tstring = NULL;

   mainENTRY("3dSpaceTimeCorr2"); machdep(); 
  
   // fill option struct with defaults
   InOpts = set_stcorr2_defaults();

   // ****************************************************************
   //                  parse command line arguments
   // ****************************************************************
	
   if (argc == 1) { usage_3dstcorr2(); exit(0); }

   /* scan through args */
   iarg = 1; 
   while( iarg < argc && argv[iarg][0] == '-' ){

      if( strcmp(argv[iarg],"-help") == 0 || 
          strcmp(argv[iarg],"-h") == 0 ) {
         usage_3dstcorr2();
         exit(0);
      }
			 
      if( strcmp(argv[iarg],"-insetA") == 0 ){
         if( ++iarg >= argc ) 
            ERROR_exit("Need argument after '%s'", argv[iarg-1]);

         InOpts.insetA = strdup(argv[iarg]) ;
         iarg++ ; continue ;
      }

      if( strcmp(argv[iarg],"-insetB") == 0 ){
         if( ++iarg >= argc ) 
            ERROR_exit("Need argument after '%s'", argv[iarg-1]);

         InOpts.insetB = strdup(argv[iarg]) ;
         iarg++ ; continue ;
      }

      if( strcmp(argv[iarg],"-mask") == 0 ){
         if( ++iarg >= argc ) 
            ERROR_exit("Need argument after '%s'", argv[iarg-1]);

         InOpts.mask_name = strdup(argv[iarg]) ;			
         iarg++ ; continue ;
      }

      if( strcmp(argv[iarg],"-prefix") == 0 ){
         if( ++iarg >= argc ) 
            ERROR_exit("Need argument after '%s'", argv[iarg-1]);

         InOpts.prefix = strdup(argv[iarg]) ;

         if( !THD_filename_ok(InOpts.prefix) ) 
            ERROR_exit("Illegal name after '%s'", argv[iarg-1]);
         iarg++ ; continue ;
      }

      if( strcmp(argv[iarg],"-out_Zcorr") == 0 ){
         InOpts.out_Zcorr = 1;
         iarg++ ; continue ;
      }

      if( strcmp(argv[iarg],"-verb") == 0 ){
         if( ++iarg >= argc ) 
            ERROR_exit("Need argument after '%s'", argv[iarg-1]);

         InOpts.verb = atoi(argv[iarg]);
         iarg++ ; continue ;
      }

      ERROR_message("Bad option '%s'\n",argv[iarg]);
      suggest_best_prog_option(argv[0], argv[iarg]);
      exit(1);
   }
	
   // ****************************************************************
   //               verify presence+behavior of inputs
   // **************************************************************** 

   INFO_message("3dSpaceTimeCorr2: verify inputs");

   if ( !InOpts.insetA ) { 
      ERROR_message("You need to provide an input dset with '-insetA ..'");
      exit(1);
   }

   if ( !InOpts.insetB ) { 
      ERROR_message("You need to provide an input dset with '-insetB ..'");
      exit(1);
   }

   if ( !InOpts.prefix )
      ERROR_exit("Need an output name via '-prefix ..'\n");

   if ( !InOpts.mask_name )
      ERROR_exit("You need to provide a mask dset with '-mask ..'");
   
   // DONE FILLING, now do the work
   ii = run_stcorr2(1, InOpts, argc, argv);

   return 0;
}

